import UIKit

/// Extension to provide some pre-defined colors to use throughout the app
extension UIColor {
    /// Text color used for major headings on the home page and elsewhere
    static var homeText: UIColor {
        UIColor(red: 0x95/0xff, green: 0x5A/0xff, blue: 0x08/0xff, alpha: 1)
    }
    
    /// The primary theme color to use for background color
    static var primary: UIColor {
        UIColor(red: 0xe9/0xff, green: 0xaf/0xff, blue: 0x00/0xff, alpha: 1)
    }
}
